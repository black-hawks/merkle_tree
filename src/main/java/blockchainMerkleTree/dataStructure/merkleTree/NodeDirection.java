package blockchainMerkleTree.dataStructure.merkleTree;

/**
 An enumeration representing the possible directions of a node in a binary tree structure.
 Each node in a binary tree can have a left and/or right child node, and this enumeration defines
 the possible directions in which a node can have a child node.
 **/
 public enum NodeDirection {

     //The left child direction of a node in a binary tree.
    LEFT,
    // The right child direction of a node in a binary tree.
    RIGHT
 }